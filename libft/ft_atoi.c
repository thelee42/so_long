#include	"libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	c;
	int	m;

	i = 0;
	c = 0;
	m = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\v' || str[i] == '\r' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			m = m * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		c = (c * 10) + (str[i] - '0');
		i++;
	}
	return (c * m);
}
